/* Costanti di round (Rcon) */
static const uint8_t rcon[11] = {
    0x00, 0x01, 0x02, 0x04, 0x08, 0x10,
    0x20, 0x40, 0x80, 0x1b, 0x36
};

/* Moltiplicazione nel campo GF(2^8) */
static uint8_t gf_mul(uint8_t a, uint8_t b) {
    uint8_t p = 0;
    for (int i = 0; i < 8; i++) {
        if (b & 1) p ^= a;
        uint8_t carry = a & 0x80;
        a <<= 1;
        if (carry) a ^= 0x1b;
        b >>= 1;
    }
    return p;
}

/* Espansione della chiave: genera 11 round keys da 16 byte ciascuna */
static void key_expansion(const uint8_t key[16], uint8_t round_keys[11][16]) {
    memcpy(round_keys[0], key, 16);

    for (int round = 1; round <= 10; round++) {
        uint8_t *prev = round_keys[round - 1];
        uint8_t *curr = round_keys[round];
        uint8_t temp[4];

        /* RotWord + SubWord + Rcon sul primo word */
        temp[0] = sbox[prev[13]] ^ rcon[round];
        temp[1] = sbox[prev[14]];
        temp[2] = sbox[prev[15]];
        temp[3] = sbox[prev[12]];

        curr[0]  = prev[0]  ^ temp[0];
        curr[1]  = prev[1]  ^ temp[1];
        curr[2]  = prev[2]  ^ temp[2];
        curr[3]  = prev[3]  ^ temp[3];

        for (int i = 4; i < 16; i++)
            curr[i] = prev[i] ^ curr[i - 4];
    }
}

/* SubBytes: sostituisce ogni byte con il valore nella S-Box */
static void sub_bytes(uint8_t state[16]) {
    for (int i = 0; i < 16; i++)
        state[i] = sbox[state[i]];
}

static void inv_sub_bytes(uint8_t state[16]) {
    for (int i = 0; i < 16; i++)
        state[i] = inv_sbox[state[i]];
}

/*
 * ShiftRows: ruota le righe della matrice 4x4.
 * Lo stato è memorizzato column-major: state[col*4 + row].
 */
static void shift_rows(uint8_t state[16]) {
    uint8_t tmp;
    /* Riga 1: rotazione sinistra di 1 */
    tmp = state[1]; state[1] = state[5]; state[5] = state[9]; state[9] = state[13]; state[13] = tmp;
    /* Riga 2: rotazione sinistra di 2 */
    tmp = state[2]; state[2] = state[10]; state[10] = tmp;
    tmp = state[6]; state[6] = state[14]; state[14] = tmp;
    /* Riga 3: rotazione sinistra di 3 (= destra di 1) */
    tmp = state[15]; state[15] = state[11]; state[11] = state[7]; state[7] = state[3]; state[3] = tmp;
}

static void inv_shift_rows(uint8_t state[16]) {
    uint8_t tmp;
    /* Riga 1: rotazione destra di 1 */
    tmp = state[13]; state[13] = state[9]; state[9] = state[5]; state[5] = state[1]; state[1] = tmp;
    /* Riga 2: rotazione destra di 2 */
    tmp = state[2]; state[2] = state[10]; state[10] = tmp;
    tmp = state[6]; state[6] = state[14]; state[14] = tmp;
    /* Riga 3: rotazione destra di 3 (= sinistra di 1) */
    tmp = state[3]; state[3] = state[7]; state[7] = state[11]; state[11] = state[15]; state[15] = tmp;
}

/* MixColumns: moltiplica ogni colonna per la matrice AES fissa */
static void mix_columns(uint8_t state[16]) {
    for (int col = 0; col < 4; col++) {
        uint8_t *c = state + col * 4;
        uint8_t s0 = c[0], s1 = c[1], s2 = c[2], s3 = c[3];
        c[0] = gf_mul(0x02, s0) ^ gf_mul(0x03, s1) ^ s2 ^ s3;
        c[1] = s0 ^ gf_mul(0x02, s1) ^ gf_mul(0x03, s2) ^ s3;
        c[2] = s0 ^ s1 ^ gf_mul(0x02, s2) ^ gf_mul(0x03, s3);
        c[3] = gf_mul(0x03, s0) ^ s1 ^ s2 ^ gf_mul(0x02, s3);
    }
}

static void inv_mix_columns(uint8_t state[16]) {
    for (int col = 0; col < 4; col++) {
        uint8_t *c = state + col * 4;
        uint8_t s0 = c[0], s1 = c[1], s2 = c[2], s3 = c[3];
        c[0] = gf_mul(0x0e,s0) ^ gf_mul(0x0b,s1) ^ gf_mul(0x0d,s2) ^ gf_mul(0x09,s3);
        c[1] = gf_mul(0x09,s0) ^ gf_mul(0x0e,s1) ^ gf_mul(0x0b,s2) ^ gf_mul(0x0d,s3);
        c[2] = gf_mul(0x0d,s0) ^ gf_mul(0x09,s1) ^ gf_mul(0x0e,s2) ^ gf_mul(0x0b,s3);
        c[3] = gf_mul(0x0b,s0) ^ gf_mul(0x0d,s1) ^ gf_mul(0x09,s2) ^ gf_mul(0x0e,s3);
    }
}

/* AddRoundKey: XOR dello stato con la round key */
static void add_round_key(uint8_t state[16], const uint8_t rk[16]) {
    for (int i = 0; i < 16; i++)
        state[i] ^= rk[i];
}

/* ---- API pubblica ---- */

void aes128_encrypt(const uint8_t key[16],
                    const uint8_t plaintext[16],
                    uint8_t       ciphertext[16]) {
    uint8_t round_keys[11][16];
    uint8_t state[16];

    key_expansion(key, round_keys);
    memcpy(state, plaintext, 16);

    add_round_key(state, round_keys[0]);

    for (int round = 1; round <= 9; round++) {
        sub_bytes(state);
        shift_rows(state);
        mix_columns(state);
        add_round_key(state, round_keys[round]);
    }

    /* Round finale: senza MixColumns */
    sub_bytes(state);
    shift_rows(state);
    add_round_key(state, round_keys[10]);

    memcpy(ciphertext, state, 16);
}

void aes128_decrypt(const uint8_t key[16],
                    const uint8_t ciphertext[16],
                    uint8_t       plaintext[16]) {
    uint8_t round_keys[11][16];
    uint8_t state[16];

    key_expansion(key, round_keys);
    memcpy(state, ciphertext, 16);

    add_round_key(state, round_keys[10]);

    for (int round = 9; round >= 1; round--) {
        inv_shift_rows(state);
        inv_sub_bytes(state);
        add_round_key(state, round_keys[round]);
        inv_mix_columns(state);
    }

    /* Round finale: senza InvMixColumns */
    inv_shift_rows(state);
    inv_sub_bytes(state);
    add_round_key(state, round_keys[0]);

    memcpy(plaintext, state, 16);
}

/* ---- Funzioni di supporto per le password ---- */

void bytes_to_hex(const uint8_t *bytes, int len, char *hex_out) {
    for (int i = 0; i < len; i++)
        sprintf(hex_out + i * 2, "%02x", bytes[i]);
    hex_out[len * 2] = '\0';
}

void hex_to_bytes(const char *hex, uint8_t *bytes_out, int len) {
    for (int i = 0; i < len; i++) {
        unsigned int val;
        sscanf(hex + i * 2, "%02x", &val);
        bytes_out[i] = (uint8_t)val;
    }
}

/*
 * Cifra una password (max 32 char) su 2 blocchi AES-128.
 * Ogni blocco da 16 byte viene paddato con zero se la password è più corta.
 * Output: 64 char hex (2 blocchi × 16 byte × 2 hex/byte).
 */
void password_encrypt(const char *password, char *hex_out) {
    const uint8_t *key = (const uint8_t *)AES_KEY;
    uint8_t plain[32]  = {0};
    uint8_t cipher[32] = {0};

    strncpy((char *)plain, password, 31);

    aes128_encrypt(key, plain,      cipher);
    aes128_encrypt(key, plain + 16, cipher + 16);

    bytes_to_hex(cipher, 32, hex_out);
}

void password_decrypt(const char *hex_in, char *password_out) {
    const uint8_t *key = (const uint8_t *)AES_KEY;
    uint8_t cipher[32] = {0};
    uint8_t plain[32]  = {0};

    hex_to_bytes(hex_in, cipher, 32);

    aes128_decrypt(key, cipher,      plain);
    aes128_decrypt(key, cipher + 16, plain + 16);

    strncpy(password_out, (char *)plain, 32);
    password_out[32] = '\0';
}

int password_verifica(const char *password, const char *hex_salvato) {
    char decifrata[33] = {0};
    password_decrypt(hex_salvato, decifrata);
    return strcmp(password, decifrata) == 0 ? 1 : 0;
}
